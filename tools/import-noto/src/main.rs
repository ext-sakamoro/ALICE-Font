fn normalise_contours(
    contours: &[Vec<(f32, f32)>],
    bbox: (f32, f32, f32, f32),
) -> Vec<Vec<(f32, f32)>> {
    let (gx0, gy0, gx1, gy1) = bbox;
    let gw = gx1 - gx0;
    let gh = gy1 - gy0;
    let g_max = gw.max(gh);
    let scale = 1.0 / g_max;
    let ox = 0.5 - (gw * scale) * 0.5;
    let oy = 0.5 - (gh * scale) * 0.5;
    let mut out = Vec::with_capacity(contours.len());
    for c in contours {
        let mut nc = Vec::with_capacity(c.len());
        for &(x, y) in c {
            // ttf-parser y-up → ALICE-Font also y-up (baseline=0, top=h).
            // We normalize y so that y=0 is bottom of glyph world, y=1 is top.
            let nx = ox + (x - gx0) * scale;
            let ny = oy + (y - gy0) * scale;
            nc.push((nx, ny));
        }
        out.push(nc);
    }
    out
}

fn emit_char(out: &mut String, ch: char, contours: &[Vec<(f32, f32)>]) {
    use core::fmt::Write;
    write!(out, "    ('\\u{{{:04X}}}', &[", ch as u32).unwrap();
    for (i, c) in contours.iter().enumerate() {
        if i > 0 {
            out.push_str(", ");
        }
        out.push_str("&[");
        for (j, (x, y)) in c.iter().enumerate() {
            if j > 0 {
                out.push_str(", ");
            }
            write!(out, "({:.4},{:.4})", x, y).unwrap();
        }
        out.push_str("]");
    }
    out.push_str("]),\n");
}

/// Build glyph outlines into a list of closed polylines.
struct PathBuilder {
    contours: Vec<Vec<(f32, f32)>>,
    cur: Vec<(f32, f32)>,
    last: (f32, f32),
}

impl PathBuilder {
    fn new() -> Self {
        Self { contours: Vec::new(), cur: Vec::new(), last: (0.0, 0.0) }
    }

    fn bbox(&self) -> Option<(f32, f32, f32, f32)> {
        let mut min = (f32::MAX, f32::MAX);
        let mut max = (f32::MIN, f32::MIN);
        let mut found = false;
        for c in &self.contours {
            for p in c {
                min.0 = min.0.min(p.0);
                min.1 = min.1.min(p.1);
                max.0 = max.0.max(p.0);
                max.1 = max.1.max(p.1);
                found = true;
            }
        }
        if found { Some((min.0, min.1, max.0, max.1)) } else { None }
    }

    fn flush(&mut self) {
        if !self.cur.is_empty() {
            let c = std::mem::take(&mut self.cur);
            self.contours.push(c);
        }
    }
}

impl OutlineBuilder for PathBuilder {
    fn move_to(&mut self, x: f32, y: f32) {
        self.flush();
        self.cur.push((x, y));
        self.last = (x, y);
    }
    fn line_to(&mut self, x: f32, y: f32) {
        self.cur.push((x, y));
        self.last = (x, y);
    }
    fn quad_to(&mut self, x1: f32, y1: f32, x: f32, y: f32) {
        for i in 1..=QUAD_SUBDIVS {
            let t = i as f32 / QUAD_SUBDIVS as f32;
            let u = 1.0 - t;
            let px = u * u * self.last.0 + 2.0 * u * t * x1 + t * t * x;
            let py = u * u * self.last.1 + 2.0 * u * t * y1 + t * t * y;
            self.cur.push((px, py));
        }
        self.last = (x, y);
    }
    fn curve_to(&mut self, x1: f32, y1: f32, x2: f32, y2: f32, x: f32, y: f32) {
        for i in 1..=CUBIC_SUBDIVS {
            let t = i as f32 / CUBIC_SUBDIVS as f32;
            let u = 1.0 - t;
            let px = u*u*u*self.last.0 + 3.0*u*u*t*x1 + 3.0*u*t*t*x2 + t*t*t*x;
            let py = u*u*u*self.last.1 + 3.0*u*u*t*y1 + 3.0*u*t*t*y2 + t*t*t*y;
            self.cur.push((px, py));
        }
        self.last = (x, y);
    }
    fn close(&mut self) {
        if let Some(&first) = self.cur.first() {
            self.cur.push(first);
        }
        self.flush();
    }
}
