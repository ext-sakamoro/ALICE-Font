//! import-noto — Extract Noto Sans JP outlines for a target character set and
//! emit `src/glyph/noto_outlines_data.rs` (Rust const, no_std-compatible).
//!
//! Outlines are flattened to polylines (Bezier subdivided), normalised into
//! the unit square `[0, 1] × [0, 1]` with the glyph centred. The rasterizer
//! in `glyph::noto_render` consumes this data.
//!
//! Not used at runtime — produces a const file that `ALICE-Font` compiles in.
//!
//! Usage:
//!   cargo run --release        # imports the default char list
//!   cargo run --release -- 株 式  # imports only the listed chars
//!
//! License: outline DATA is derived from Noto Sans JP (SIL OFL).
//! See LICENSES/NotoSansJP-OFL.txt for terms.
//!
//! Author: Moroya Sakamoto

use std::path::PathBuf;
use ttf_parser::{Face, OutlineBuilder};

const QUAD_SUBDIVS: usize = 8;
const CUBIC_SUBDIVS: usize = 12;

fn main() -> Result<(), Box<dyn std::error::Error>> {
    let mut chars: Vec<char> = std::env::args()
        .skip(1)
        .filter_map(|s| s.chars().next())
        .collect();

    if chars.is_empty() {
        // Default set: extoria-website 267 kanji + key radicals + kana.
        chars = default_charset();
    }
    chars.sort();
    chars.dedup();

    let manifest = PathBuf::from(env!("CARGO_MANIFEST_DIR"));
    let font_path = manifest.join("../render-ref/data/NotoSansJP-Regular.ttf");
    let font_data = std::fs::read(&font_path)?;
    let face = Face::parse(&font_data, 0)?;
    eprintln!("Loaded font {} ({} glyphs)", font_path.display(), face.number_of_glyphs());

    // Output path: src/glyph/noto_outlines_data.rs
    let out_path = manifest.join("../../src/glyph/noto_outlines_data.rs");
    let mut out = String::new();
    out.push_str("// Auto-generated by tools/import-noto. Do not edit by hand.\n");
    out.push_str("//\n");
    out.push_str("// Outline polylines for CJK characters, sourced from Noto Sans JP (SIL OFL).\n");
    out.push_str("// Coordinates are normalised into [0, 1] x [0, 1]. Each character has one or\n");
    out.push_str("// more contours; each contour is a closed polyline.\n");
    out.push_str("\n");
    out.push_str("/// (codepoint, contour-list) entries. Sorted ascending for binary search.\n");
    out.push_str("/// Each contour is a slice of `(f32, f32)` points; consecutive points form\n");
    out.push_str("/// edges; the last point equals the first to close the contour.\n");
    out.push_str("pub static NOTO_OUTLINES: &[(char, &[&[(f32, f32)]])] = &[\n");

    let mut emitted = 0usize;
    let mut skipped = 0usize;
    for ch in &chars {
        let Some(glyph_id) = face.glyph_index(*ch) else {
            skipped += 1;
            continue;
        };
        let mut builder = PathBuilder::new();
        if face.outline_glyph(glyph_id, &mut builder).is_none() {
            skipped += 1;
            continue;
        }
        if builder.contours.is_empty() {
            skipped += 1;
            continue;
        }
        let bbox = builder.bbox().unwrap();
        let normalised = normalise_contours(&builder.contours, bbox);
        emit_char(&mut out, *ch, &normalised);
        emitted += 1;
    }
    out.push_str("];\n");

    std::fs::write(&out_path, &out)?;
    eprintln!("Wrote {} ({} chars, {} skipped)", out_path.display(), emitted, skipped);
    Ok(())
}

